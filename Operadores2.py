# Operadores Booleanos
# < <=
print(10 < 10)
print(10 <= 10)
# > >=
print(10 > 10)
print(10 >= 10)
# ==
print(10 == 10)
# !=
print(10 != 10)

# operadores Logicos
print("Operadotes logicos")
# and (true if all false )
print (True and True)
print(True and False)

# or (true if at least one true)
print(False or False)
print(False or True)

# not true
print (not True)
print (not False)